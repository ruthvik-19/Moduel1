
#User input converted from string to integer
num1 = int(input("Enter the first number "))
num2 = int(input("Enter the second number "))

#variable that holds the value of the product of num1 and num2
mult = num1 * num2
#variable that holds the value of the quotient of num1 and num2
div = num1 / num2

#formatted print statment
print("The product of {} and {} is {}".format(num1, num2, mult))
print("The quotient of {} and {} is {}".format(num1, num2, div))